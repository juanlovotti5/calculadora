    private void jButton28ActionPerformed(java.awt.event.ActionEvent evt) {                                          
        this.n2=Float.parseFloat(this.casilla.getText());
        
        switch(this.operador){
            case "+": this.casilla.setText(sincero(this.n1 + this.n2)); break;
            case "-": this.casilla.setText(sincero(this.n1 - this.n2)); break;
            case "*": this.casilla.setText(sincero(this.n1 * this.n2)); break;
            case "/": if(this.n2 == 0){this.casilla.setText("Error, no se puede dividir por cero.");}
            else{
                this.casilla.setText(sincero(this.n1 / this.n2)); break;
            }   
        }
    }                                         

